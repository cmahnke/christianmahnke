import { readFileSync } from 'fs';
import { extname, resolve, dirname } from 'path';
import type { Plugin } from 'rollup';

import * as pkg from 'brotli-unicode';
const { compress } = (pkg as any);

const WASM_URL_PATTERN = /new URL$['"]([^'"]+\.wasm)['"]\s*,\s*import\.meta\.url$/g;

export async function inlineWasm(): Promise<Plugin> {
  return {
    name: 'inline-wasm',

    async resolveId(source, importer) {
      if (extname(source) !== '.wasm') return null;

      if (importer) {
        return resolve(dirname(importer), source);
      }

      return resolve(source);
    },

    async load(id) {
      if (extname(id) !== '.wasm') return null;

      const wasmBuffer = readFileSync(id);
      const compressed = await compress(new Uint8Array(wasmBuffer));
      const serialized = JSON.stringify(Array.from(compressed));

      return buildWasmModule(serialized, id);
    },

    async transform(code, id) {
      if (extname(id) === '.wasm') return null;
      if (!WASM_URL_PATTERN.test(code)) return null;
      WASM_URL_PATTERN.lastIndex = 0;

      let transformedCode = code;
      const wasmImports: string[] = [];

      for (const match of code.matchAll(WASM_URL_PATTERN)) {
        const [fullMatch, wasmPath] = match;
        const absoluteWasmPath = resolve(dirname(id), wasmPath);
        const importName = `__wasm_${wasmImports.length}`;

        wasmImports.push(
          `import { getWasmBytes as ${importName} } from '${absoluteWasmPath}';`
        );

        transformedCode = transformedCode.replace(
          fullMatch,
          `URL.createObjectURL(new Blob([${importName}()], { type: 'application/wasm' }))`
        );
      }

      transformedCode = wasmImports.join('\n') + '\n' + transformedCode;

      return {
        code: transformedCode,
        map: { mappings: '' },
      };
    },
  };
}

function buildWasmModule(serialized: string, sourceId: string): string {
  return `
// Auto-generated by rollup-plugin-inline-wasm
// Source: ${sourceId}

import * as pkg from 'brotli-unicode';
const { decompress } = (pkg as any);

const compressed = new Uint8Array(${serialized});

export async function loadWasm(
  imports?: WebAssembly.Imports
): Promise<WebAssembly.Instance> {
  const wasmBytes = decompress(compressed);
  const { instance } = await WebAssembly.instantiate(wasmBytes, imports);
  return instance;
}

export function getWasmBytes(): Uint8Array {
  return decompress(compressed);
}

export default loadWasm;
`.trim();
}